package client;

public class ClientBean {
	private String client_idx;        
	private String client_name;        
	private String client_ceo;        
	private String client_tel;        
	private String client_zipcode;        
	private String client_addr;
	
	public String getClient_idx() {
		return client_idx;
	}
	public void setClient_idx(String client_idx) {
		this.client_idx = client_idx;
	}
	public String getClient_name() {
		return client_name;
	}
	public void setClient_name(String client_name) {
		this.client_name = client_name;
	}
	public String getClient_ceo() {
		return client_ceo;
	}
	public void setClient_ceo(String client_ceo) {
		this.client_ceo = client_ceo;
	}
	public String getClient_tel() {
		return client_tel;
	}
	public void setClient_tel(String client_tel) {
		this.client_tel = client_tel;
	}
	public String getClient_zipcode() {
		return client_zipcode;
	}
	public void setClient_zipcode(String client_zipcode) {
		this.client_zipcode = client_zipcode;
	}
	public String getClient_addr() {
		return client_addr;
	}
	public void setClient_addr(String client_addr) {
		this.client_addr = client_addr;
	}  
	
}
